// return (dot(rgb, (float3)(1.0f, 1.0f, 1.0f)));
float	luma(float3 rgb)
{
	return (dot(rgb, (float3)(0.2126f, 0.7152f, 0.0722f)));
}

float3	linear_to_gamma(float3 x)
{
	return (sqrt(x));
}
